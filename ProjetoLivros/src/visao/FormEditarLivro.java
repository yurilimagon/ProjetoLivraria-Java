    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (jTextFieldTitulo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite o Título.");
            jTextFieldTitulo.requestFocus();
        } else if (jTextFieldAutor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite o Autor.");
            jTextFieldAutor.requestFocus();
        } else if (jTextFieldEditora.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite a Editora.");
            jTextFieldEditora.requestFocus();
        } else if (jTextFieldPaginas.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite a quantidade de Páginas.");
            jTextFieldPaginas.requestFocus();
        } else if ((jTextFieldSequencia.getText().isEmpty()) && check) {
            if (check == true) {
                JOptionPane.showMessageDialog(null, "Digite o nome da Sequência.");
                jTextFieldSequencia.requestFocus();
            } else {
                mod.setSequencia("");
            }
        } else if ((jTextFieldNumSequencia.getText().isEmpty()) && check) {
            if (check == true) {
                JOptionPane.showMessageDialog(null, "Digite o número da Sequência.");
                jTextFieldNumSequencia.requestFocus();
            } else {
                mod.setNumerosequencia(0);
            }
        } else {
            mod.setCod(Integer.parseInt(jTextFieldCodLivro.getText()));
            mod.setTitulo(jTextFieldTitulo.getText());
            mod.setAutor(jTextFieldAutor.getText());
            mod.setEditora(jTextFieldEditora.getText());
            mod.setPaginas(Integer.parseInt(jTextFieldPaginas.getText()));
            mod.setFormato((String) jComboBoxFormato.getSelectedItem());
            if (checkBox) {
                //if(jComboBoxFormato.getSelectedItem().equals(""));
                mod.setBox("Sim");
            } else {
                mod.setBox("Não");
            }
            mod.setLido((String) jComboBoxLido.getSelectedItem());
            if (check == true) {
                mod.setSequencia(jTextFieldSequencia.getText());
                mod.setNumerosequencia(Integer.parseInt(jTextFieldNumSequencia.getText()));
            } else {
                mod.setSequencia("");
                mod.setNumerosequencia(0);
            }
            control.editar(mod);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

