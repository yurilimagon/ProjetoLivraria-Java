    public void exportaPesquisa(FormPesquisarEditar model, String search) {
        jTextFieldPesquisaEditar.setText(search);
        //jLabelPesquisa.setVisible(false);

        con.conexao();

        //con.executaSql("select *from livros where titulo_livro like'%" + String.valueOf(jTextFieldPesquisaEditar.getText()) + "%'");
        con.executaSql("select *from livros where titulo_livro='" + jTextFieldPesquisaEditar.getText() + "'");
        //con.executaSql("select *from livros where titulo_livro like'" + jTextFieldPesquisa.getText() + "'");
        //con.executaSql("select *from livros order by cod_livro");

        try {
            con.rs.first();
            jTextFieldCodLivro.setText(String.valueOf(con.rs.getInt("cod_livro")));
            jTextFieldTitulo.setText(con.rs.getString("titulo_Livro"));
            jTextFieldAutor.setText(con.rs.getString("autor_livro"));
            jTextFieldEditora.setText(con.rs.getString("editora_livro"));
            jTextFieldPaginas.setText(String.valueOf(con.rs.getInt("paginas_livro")));
            jComboBoxFormato.setSelectedItem(con.rs.getString("formato_livro"));
            if ("Sim".equals(con.rs.getString("box_livro"))) {
                jCheckBoxBoxEditar.setSelected(true);
            } else {
                if (con.rs.getString("formato_livro").equals("Físico")) {
                    jCheckBoxBoxEditar.setEnabled(true);
                    jCheckBoxBoxEditar.setSelected(false);
                } else {
                    jCheckBoxBoxEditar.setEnabled(false);
                }
            }
            jComboBoxLido.setSelectedItem(con.rs.getString("lido_livro"));
            if (!"".equals(con.rs.getString("sequencia_livro"))) {
                jCheckBoxSeq.setSelected(true);
                jTextFieldSequencia.setText(con.rs.getString("sequencia_livro"));
                jTextFieldNumSequencia.setText(String.valueOf(con.rs.getInt("numseq_livro")));
                check = true;
            } else {
                jCheckBoxSeq.setSelected(false);
                jTextFieldSequencia.setEnabled(false);
                jTextFieldNumSequencia.setEnabled(false);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Selecione um Livro na Tabela.");
        }

        con.desconecta();
    }

