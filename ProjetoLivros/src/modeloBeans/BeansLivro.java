public class BeansLivro {

    //ATRIBUTOS
    private int cod;
    private String titulo;
    private String autor;
    private String editora;
    private int paginas;
    private String formato;
    private String box;
    private String lido;
    private String sequencia;
    private int numerosequencia;
    private String pesquisa;
    private int pesquisaCod;

    //GETTERS E SETTERS
    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getEditora() {
        return editora;
    }

    public void setEditora(String editora) {
        this.editora = editora;
    }

    public int getPaginas() {
        return paginas;
    }

    public void setPaginas(int paginas) {
        this.paginas = paginas;
    }

    public String getFormato() {
        return formato;
    }

    public void setFormato(String formato) {
        this.formato = formato;
    }

    public String getBox() {
        return box;
    }

    public void setBox(String box) {
        this.box = box;
    }

    public String getLido() {
        return lido;
    }

    public void setLido(String lido) {
        this.lido = lido;
    }

    public String getSequencia() {
        return sequencia;
    }

    public void setSequencia(String sequencia) {
        this.sequencia = sequencia;
    }

    public int getNumerosequencia() {
        return numerosequencia;
    }

    public void setNumerosequencia(int numerosequencia) {
        this.numerosequencia = numerosequencia;
    }

    public String getPesquisa() {
        return pesquisa;
    }

    public void setPesquisa(String pesquisa) {
        this.pesquisa = pesquisa;
    }

    public int getPesquisaCod() {
        return pesquisaCod;
    }

    public void setPesquisaCod(int pesquisaCod) {
        this.pesquisaCod = pesquisaCod;
    }

}
